		           Larger width sizes like 55 to 85 mm roll faster, and move more easily over cracks in pavement and are better for transition skateboarding. 
		           Smaller width sizes like 48 to 54 mm keep the board closer to the ground, require less force to accelerate and produce a lower center of gravity 
		           which allows for a better response time, but also make for a  slower top speed and are better for street skateboarding.</p>
