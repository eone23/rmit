		        <h2>Trucks</h2>
		        <img src="images/truck.jpg" alt="trucks" class="center">
			         <p>Attached to the deck are two metal (usually aluminum alloy) trucks, which connect the wheels and bearings to the deck. 
		           The trucks are further composed of two parts. The top part of the truck is screwed to the deck and is called the baseplate, and beneath it is the hanger. 
		           The axle runs through the hanger. Between the baseplate and the hanger are bushings, also rubbers or grommets, that provide the cushion mechanism for turning the skateboard. 
		           The bushings cushion the truck when it turns. The stiffer the bushings, the more resistant the skateboard is to turning. 
		           The softer the bushings, the easier it is to turn. A bolt called a kingpin holds these parts together and fits inside the bushings. 
		           Thus by tightening or loosening the kingpin nut, the trucks can be adjusted loosely for better turning and tighter for more stability</p>			
			
